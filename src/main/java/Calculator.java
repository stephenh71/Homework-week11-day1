public class Calculator {


    public int Add(int n1, int n2) {
        return (n1 + n2);
    }

    public int Subtract(int n1, int n2) {
        return (n1 - n2);
    }

    public int Multiply(int n1, int n2) {
        return (n1 * n2);
    }

    public double Divide(int n1, int n2) {
        return (n1/n2);
    }

}